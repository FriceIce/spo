import OpenAI from "openai";

const openAI = new OpenAI({
  apiKey: process.env.OPENAI_API_KEY,
});

export const getGenresForPrompt = async (prompt: string) => {
  const errorMessage = "No response from OpenAI API";

  try {
    const chatResponse = await openAI.chat.completions.create({
      messages: [
        {
          role: "system",
          content:
            "You are an AI that helps users select music genres. Your task is to give a short explanation followed by a list of five relevant genres. Do not include numbers, commas, or any extra text in the genre listâ€”just separate each genre with a line break!",
        },
        {
          role: "user",
          content: `Here is a list of genres: ${genresSeeds} Based on the following prompt, provide five relevant genres at the end: "${prompt}". Provide a brief explanation first, and then list the genres, each on a new line with no additional characters.`,
        },
      ],
      model: "gpt-3.5-turbo",
      max_tokens: 250,
    });

    if (!chatResponse) throw new Error(errorMessage);

    const fullResponse = String(chatResponse.choices[0].message.content);
    const [explanation, genres] = fullResponse.split(":\n\n");

    return {
      explanation,
      genres: genres.split("\n").filter((genre) => genre !== ""),
    };
  } catch (err) {
    console.log(errorMessage);
    return null;
  }
};

